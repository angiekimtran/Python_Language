# program comparing values with logical and relational operators

# logical operators: [and, or, not]

# Program 4 with or
x = int(input("What's x? "))
y = int(input("What's y? "))

if x < y or x > y:
    print("x is not equal to y")
else: 
    print("x is equal to y")

# Program 4 with !=
x = int(input("What's x? "))
y = int(input("What's y? "))

if x != y:
    print("x is not equal to y")
else: 
    print("x is equal to y")

# Program 5 with ==
x = int(input("What's x? "))
y = int(input("What's y? "))

if x == y:
    print("x is equal to y")
else: 
    print("x is not equal to y")