# Practice programs with a while loop
# While is a construct that allows a question to be asked again and again

# Program 1 - program that prints out "meow" three times
i = 3
while i != 0:
    print("meow")
    i = i - 1

# Program 2 - another way to code program 1
i = 1
while i <= 3: # Starts at 1 and goes up to 3 (inclusive)
    print("meow")
    i = i + 1

# Program 3 - another way to code program 1
i = 0
while i < 3: # Starts at 0 and goes up to but not through 3
    print("meow")
    i += 1
